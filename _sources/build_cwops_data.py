"""Parse the CWA Intermediate HTML and emit data/cwops-intermediate.js.

The source HTML uses Microsoft Office's "save as web" output, which is full of
<o:p>, mso-* style attributes, and stray &nbsp; runs. We clean those out while
preserving headings, paragraphs, lists, and hyperlinks.
"""
from __future__ import annotations

import json
import re
from pathlib import Path

ROOT = Path(__file__).parent.parent
SRC = ROOT / "_sources" / "cwops-int.htm"
AUDIO_MAP = ROOT / "_sources" / "cwops-practice-audio.json"
OUT = ROOT / "data" / "cwops-intermediate.js"


# In-prose codes the curriculum uses to reference practice files. Each pattern
# captures a code that can be looked up (after normalization) in the audio map
# generated by extract_practice_audio.py.
CODE_PATTERN = re.compile(
    r"\b("
    r"WD\d+-\d+"
    r"|PR\d+-\d+"
    r"|SS\d+-\d+"
    r"|POTA\d+-\d+"
    r"|CWT[-\s]?\d+[-\s]?\d+"
    r"|QSO_?\d+-\d+"
    r"|(?:DIS|UN|IM|IN|RE|IR|ING|ES|ED|LY)\d+-\d+"
    r")\b",
    re.I,
)


def load_audio_map() -> dict[str, str]:
    if not AUDIO_MAP.exists():
        return {}
    return json.loads(AUDIO_MAP.read_text())


def lookup_audio(code: str, audio: dict[str, str]) -> str | None:
    """Try a few normalization variants to find the audio URL for a code."""
    # Replace whitespace with hyphen and uppercase.
    base = re.sub(r"\s+", "-", code).upper()
    candidates = [base]
    # Insert a hyphen between an alphabetic prefix and a digit run if missing.
    m = re.match(r"^([A-Z]+)(\d+.*)$", base)
    if m and not base.startswith(m.group(1) + "-"):
        candidates.append(f"{m.group(1)}-{m.group(2)}")
    # Underscore→hyphen variant of the above.
    candidates.extend(c.replace("_", "-") for c in list(candidates))
    # Hyphen→underscore between alpha and digits (some codes mix).
    for c in list(candidates):
        candidates.append(c.replace("-", "_"))
    # De-dup preserving order.
    seen, ordered = set(), []
    for c in candidates:
        if c not in seen:
            ordered.append(c)
            seen.add(c)
    for c in ordered:
        if c in audio:
            return audio[c]
    return None


def linkify_audio_codes(html: str, audio: dict[str, str]) -> tuple[str, list[dict]]:
    """Wrap practice-file codes in clickable mp3 links and collect them as tools."""
    found: list[dict] = []
    seen_keys: set[str] = set()

    def replace(m):
        code = m.group(1)
        url = lookup_audio(code, audio)
        if not url:
            return code
        key = (code.upper().replace(" ", "-"), url)
        if key not in seen_keys:
            seen_keys.add(key)
            found.append({"name": code, "url": url})
        return f'<a class="audio" href="{url}" target="_blank" rel="noopener">{code}</a>'

    # Avoid replacing inside existing <a>...</a> by splitting on tag boundaries.
    parts = re.split(r"(<a\b[^>]*>.*?</a>)", html, flags=re.S | re.I)
    for i, p in enumerate(parts):
        if i % 2 == 0:  # outside <a>
            parts[i] = CODE_PATTERN.sub(replace, p)
    return "".join(parts), found


def add_target_blank(html: str) -> str:
    """Ensure all <a href> tags in the rendered prose open in a new tab."""
    def fix(m):
        tag = m.group(0)
        if "target=" in tag.lower():
            return tag
        return tag[:-1] + ' target="_blank" rel="noopener">'
    return re.sub(r"<a\b[^>]*>", fix, html, flags=re.I)


